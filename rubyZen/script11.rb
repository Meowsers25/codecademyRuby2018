# If we know the range of numbers we'd like
# to include, we can use .upto and .downto.

# 95.upto(100) { |num| print num, " " }
# Prints 95 96 97 98 99 100

# and we can use .downto to do the same thing
# with descending values.

 # Use .upto to puts the capital letters "L"
 # through "P".

 "L".upto("P"){ |letter| puts letter}
